class ArithmaticOperation{
	public static void main(String [] args){
		int x = 110;
		int y = 20 , z;
		
		z = x + y; 
		System.out.println("sum is = " + z);

		z = x - y;
		System.out.println("sub is = " + z);

		z = x * y;
		System.out.println("mul is = " + z);

		z = x / y;
		System.out.println("div is = " + z);

		z = x % y;
		System.out.println("mod is = " + z);

	}
}