class FloatingPoint{
	public static void main(String [] args){
		
		System.out.println(10.24);	
		System.out.println(0.00002);	//prints 2.0E-5
		System.out.println(1.2E-3);	//prints 0.0012
		System.out.println(1.2E+8);	//prints 1.2E8
		System.out.println(1350000000000000.0);	//prints 1.35E15
	}
}