class IncrementDecrement{
	public static void main(String [] args){
		int x = 100;					
		System.out.println(x);		//prints 100	
	
		x++;				//post incrementer (x = x+1)
		System.out.println(x);		//print 101

		++x;				//pre incrementer 
		System.out.println(x);		//print 102

		x--;				//post incrementer (x = x-1)
		System.out.println(x);		//print 101

		--x;				//pre incrementer 
		System.out.println(x);		//print 100
		

	}
}