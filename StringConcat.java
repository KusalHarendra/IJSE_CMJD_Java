class StringConcat{
	public static void main(String [] args){
		
		System.out.println(10 + 20);		//prints 30
		System.out.println("10" + "20");	//prints 1020
		System.out.println("AB" + "CD");	//prints ABCD	
		System.out.println("AB" + 10);		//prints AB10
		System.out.println("20" + 10);		//prints 2010

	}
}