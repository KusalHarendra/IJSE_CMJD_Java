class StringConcat2{
	public static void main(String [] args){
		int x = 10, y = 20;		

		System.out.println("x + y");		//prints x + y
		System.out.println("x" + y);		//prints x20
		System.out.println(x + "y");		//prints 10y	
		System.out.println(x + y);		//prints 30
		System.out.println("x" + "y");		//prints xy

	}
}